def fix_geocoding(input_file, output_file, changes_log, anomalies_report):
    print(f"\n{'='*70}")
    print("GHANA SCHOOLS GEOCODING CORRECTION")
    print(f"Started: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print(f"{'='*70}")

    df = pd.read_csv(input_file)
    print(f"\n📂 Loaded {len(df)} schools from {input_file}")

    # Standardize region names
    df['region'] = df['region'].replace(REGION_MAP).str.upper().str.strip()

    changes = []

    for idx, row in df.iterrows():
        name = row['school_name']
        region = row['region']
        old_lat = row.get('latitude')
        old_lon = row.get('longitude')

        new_lat, new_lon, method = None, None, None

        # 1. School-level override (highest priority)
        if name in SCHOOL_OVERRIDES:
            new_lat = SCHOOL_OVERRIDES[name]['lat']
            new_lon = SCHOOL_OVERRIDES[name]['lon']
            method = 'SCHOOL_OVERRIDE'

        # 2. Town gazetteer via `location` column
        elif pd.notna(row.get('location')):
            loc = str(row['location']).upper().strip()
            if loc in TOWN_COORDS:
                new_lat, new_lon = TOWN_COORDS[loc]
                method = f'TOWN_LOOKUP:{loc}'

        if new_lat is not None:
            df.at[idx, 'latitude'] = new_lat
            df.at[idx, 'longitude'] = new_lon
            changes.append({
                'school': name, 'region': region, 'method': method,
                'old_lat': old_lat, 'old_lon': old_lon,
                'new_lat': new_lat, 'new_lon': new_lon,
            })

    print(f"✓ Applied {len(changes)} corrections")

    # Save corrected file
    os.makedirs(os.path.dirname(output_file), exist_ok=True)
    df.to_csv(output_file, index=False)
    print(f"💾 Saved to {output_file}")

    # Save changes log
    os.makedirs(os.path.dirname(changes_log), exist_ok=True)
    pd.DataFrame(changes).to_csv(changes_log, index=False)

    # Validate
    anomalies = []
    for _, row in df.iterrows():
        region = row['region']
        lat, lon = row.get('latitude'), row.get('longitude')
        if region not in REGION_BOUNDS:
            continue
        if pd.isna(lat) or pd.isna(lon):
            anomalies.append({'school': row['school_name'], 'region': region,
                               'lat': lat, 'lon': lon, 'reason': 'MISSING'})
            continue
        lb = REGION_BOUNDS[region]
        if not (lb['lat'][0] <= lat <= lb['lat'][1]):
            anomalies.append({'school': row['school_name'], 'region': region,
                               'lat': lat, 'lon': lon,
                               'reason': f"LAT {lat:.4f} outside {lb['lat']}"})
        elif not (lb['lon'][0] <= lon <= lb['lon'][1]):
            anomalies.append({'school': row['school_name'], 'region': region,
                               'lat': lat, 'lon': lon,
                               'reason': f"LON {lon:.4f} outside {lb['lon']}"})

    pd.DataFrame(anomalies).to_csv(anomalies_report, index=False)

    print(f"\n{'='*70}")
    print(f"VALIDATION RESULTS")
    print(f"{'='*70}")
    print(f"Total corrections applied : {len(changes)}")
    print(f"Remaining anomalies       : {len(anomalies)}")
    if anomalies:
        print(f"\n⚠️  First 10:")
        for a in anomalies[:10]:
            print(f"   - {a['school']} ({a['region']}): {a['reason']}")
        print(f"\n📄 Full report: {anomalies_report}")
    else:
        print("\n✅ All coordinates valid!")
